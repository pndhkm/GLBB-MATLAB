function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
%input
v0=str2double(get(handles.edit1,'string'));
vt=str2double(get(handles.edit2,'string'));
s=str2double(get(handles.edit3,'string'));

%proses perhitungan
conv0 = (v0*1000) / 3600;
convt = (vt*1000) / 3600;

%percepatan
a1 = convt^2 - conv0^2;
a2 = s * 2;
a = a1 / a2;

%waktu tempuh
t = (convt - conv0) / a;

%  hasil
set(handles.edit4,'string',a);
set(handles.edit5,'string',t);
set(handles.edit6,'string',a);
set(handles.edit7,'string',t);

